// ============ i18n Engine ============

const i18n = {
  currentLang: 'en',

  init() {
    const saved = localStorage.getItem('lang');
    const urlParam = new URLSearchParams(window.location.search).get('lang');
    this.currentLang = urlParam || saved || 'en';
    this.apply();
    this.updateToggle();
  },

  setLang(lang) {
    this.currentLang = lang;
    localStorage.setItem('lang', lang);
    document.documentElement.lang = lang;
    this.apply();
    this.updateToggle();
  },

  apply() {
    const elements = document.querySelectorAll('[data-i18n]');
    elements.forEach(el => {
      const key = el.getAttribute('data-i18n');
      const text = translations[this.currentLang]?.[key];
      if (text) {
        if (el.tagName === 'INPUT' || el.tagName === 'TEXTAREA') {
          el.placeholder = text;
        } else {
          el.textContent = text;
        }
      }
    });
    document.documentElement.lang = this.currentLang;
  },

  updateToggle() {
    document.querySelectorAll('.lang-toggle__option').forEach(opt => {
      opt.classList.toggle('lang-toggle__option--active', opt.dataset.lang === this.currentLang);
    });
  },

  toggle() {
    this.setLang(this.currentLang === 'en' ? 'es' : 'en');
  }
};

// Initialize on DOM ready
document.addEventListener('DOMContentLoaded', () => {
  i18n.init();

  // Language toggle click
  document.getElementById('langToggle')?.addEventListener('click', () => {
    i18n.toggle();
  });
});
